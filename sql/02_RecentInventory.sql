-- ============================================================
-- 02_RecentInventory.sql
-- Power BI 用的輕量庫存水位表｜7,848,102 rows
-- ============================================================
--
-- [建構邏輯]
-- DailyInventorySnapshot 有 7,500 萬筆，全部匯入 Power BI 效能不佳。
-- 此表僅萃取儀表板播放軸所需的日期範圍，資料量減少約 90%。
--
-- [日期範圍設計]
-- 儀表板播放軸從 2016-12-01 開始，但「近 7 日平均日銷量」需回溯 7 天，
-- 因此起始日為 11/24（12/1 往前推 7 天的緩衝期）。
--
-- 💡 實務資料工程中，當計算邏輯含滾動視窗（moving window），
--    來源資料必須多保留一個視窗大小的緩衝期，才能讓邊界條件正確運算。
-- ============================================================


INSERT INTO dbo.RecentInventory (日期, 門市編號, SKU編號, 庫存數量, 庫存金額)
SELECT
    日期,
    門市編號,
    SKU編號,
    庫存數量,
    庫存金額
FROM dbo.DailyInventorySnapshot
WHERE 日期 >= '2016-11-24'   -- 7-day lookback buffer before Dec 1
  AND 日期 <= '2016-12-31';
