-- 從 BegInventory 以 DISTINCT 取唯一的門市編號與城市組合。
-- 門市編號確認為 INT 型態，確保與事實表的 JOIN 效能最佳。
-- ============================================================


INSERT INTO dbo.Dim_Store (門市編號, 城市)
SELECT DISTINCT
    CAST(REPLACE(["Store"], '"', '') AS INT),
    REPLACE(["City"], '"', '')
FROM dbo.BegInventory;
